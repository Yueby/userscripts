const STORAGE_KEY = 'x-draw-helper-lang';
const AVAILABLE_LANGS: LangKey[] = ['en', 'zh', 'ja'];

const currentLang = ref<LangKey>(
  (localStorage.getItem(STORAGE_KEY) as LangKey) || 'en'
);

function t(key: string, params: Record<string, string | number> = {}): string {
  const text = LANGUAGES[currentLang.value][key] || LANGUAGES.en[key] || key;
  return text.replace(/\$\{(\w+)\}/g, (_, p) => String(params[p] ?? ''));
}

function setLang(lang: LangKey) {
  currentLang.value = lang;
  localStorage.setItem(STORAGE_KEY, lang);
}

export function useI18n() {
  return {
    t,
    currentLang,
    setLang,
    availableLangs: AVAILABLE_LANGS,
    getLangName: (lang: LangKey) => LANGUAGES[lang].langName,
  };
}

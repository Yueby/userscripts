/**
 * 为 SVG 图标设置尺寸和 stroke-width
 * @param svg SVG 字符串
 * @param size 尺寸（宽高相等）
 * @param strokeWidth 可选的 stroke-width
 */
export const withSize = (svg: string, size: number, strokeWidth?: number): string => {
  let result = svg.replace('<svg ', `<svg width="${size}" height="${size}" `);
  if (strokeWidth !== undefined) {
    result = result.replace(/stroke-width="[\d.]+"/g, `stroke-width="${strokeWidth}"`);
  }
  return result;
};

